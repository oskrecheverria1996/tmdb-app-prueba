import { HttpClient, HttpContext, HttpResponse } from '@angular/common/http';
import { Observable } from 'rxjs';
import { filter, map } from 'rxjs/operators';
import { StrictHttpResponse } from '../../strict-http-response';
import { RequestBuilder } from '../../request-builder';

import { Label } from '../../models/label';

export interface IssuesRemoveLabel$Params {

/**
 * The account owner of the repository. The name is not case sensitive.
 */
  owner: string;

/**
 * The name of the repository without the `.git` extension. The name is not case sensitive.
 */
  repo: string;

/**
 * The number that identifies the issue.
 */
  issue_number: number;
  name: string;
}

export function issuesRemoveLabel(http: HttpClient, rootUrl: string, params: IssuesRemoveLabel$Params, context?: HttpContext): Observable<StrictHttpResponse<Array<Label>>> {
  const rb = new RequestBuilder(rootUrl, issuesRemoveLabel.PATH, 'delete');
  if (params) {
    rb.path('owner', params.owner, {});
    rb.path('repo', params.repo, {});
    rb.path('issue_number', params.issue_number, {});
    rb.path('name', params.name, {});
  }

  return http.request(
    rb.build({ responseType: 'json', accept: 'application/json', context })
  ).pipe(
    filter((r: any): r is HttpResponse<any> => r instanceof HttpResponse),
    map((r: HttpResponse<any>) => {
      return r as StrictHttpResponse<Array<Label>>;
    })
  );
}

issuesRemoveLabel.PATH = '/repos/{owner}/{repo}/issues/{issue_number}/labels/{name}';
